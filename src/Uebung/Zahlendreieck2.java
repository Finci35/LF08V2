package Uebung;

public class Zahlendreieck2 {
    public static void main(String[] args) {
        int z = 0;
        String ausgabe = "";
        //Umsetzung mit der while Schleife
        while (z < 10){
            ausgabe += z + " ";   //ausgabe = ausgabe + z + " ";
            System.out.println(ausgabe);
            z++;
        }
    }
}
